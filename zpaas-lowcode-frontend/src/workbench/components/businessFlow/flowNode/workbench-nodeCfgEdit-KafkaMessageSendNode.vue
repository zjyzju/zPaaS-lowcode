	<el-row :gutter="4">
        <el-col :span="16">
            <el-form-item label="KafkaProducer">
                <el-select v-model="nodeCfgJson.kafkaProducerId" class="m-2" placeholder="Select" size="small">
                    <el-option v-for="item in kafkaProducerResources" :key="item.id" :label="item.name" :value="item.id" />
                </el-select>
            </el-form-item>
        </el-col>
        <el-col :span="8"></el-col>
    </el-row>
    
    <el-row :gutter="4">
        <el-col :span="16">
            <el-form-item label="消息主题来源">
                <el-select v-model="nodeCfgJson.topicSource" clearable class="m-2" placeholder="Select" size="small">
                    <el-option v-for="item in sourceOptions" :key="item.value" :label="item.label" :value="item.value" />
                </el-select>
            </el-form-item>
        </el-col>
        <el-col :span="8">
		    <el-form-item label="" label-width="10px">
                <el-link  type="primary" @click="showAvailableDataSelectPage(sourceOptions, (contextType, contextKey)=>{this.nodeCfgJson.topicSource=contextType;this.nodeCfgJson.topicKey=contextKey;})" ><label>选择</label></el-link>&nbsp;
            </el-form-item>
		</el-col>
    </el-row>
    <el-row :gutter="4">
        <el-col :span="12">
            <el-form-item label="消息主题Key">
                <el-input v-model="nodeCfgJson.topicKey" size="small" />
            </el-form-item>
        </el-col>
        <el-col :span="12">
            <el-form-item label="消息主题属性">
                <el-input v-model="nodeCfgJson.topicAttr" size="small" />
            </el-form-item>
        </el-col>
    </el-row>
    
    <el-row :gutter="4">
        <el-col :span="16">
            <el-form-item label="消息key来源">
                <el-select v-model="nodeCfgJson.keySource" clearable class="m-2" placeholder="Select" size="small">
                    <el-option v-for="item in sourceOptions" :key="item.value" :label="item.label" :value="item.value" />
                </el-select>
            </el-form-item>
        </el-col>
        <el-col :span="8">
		    <el-form-item label="" label-width="10px">
                <el-link  type="primary" @click="showAvailableDataSelectPage(sourceOptions, (contextType, contextKey)=>{this.nodeCfgJson.keySource=contextType;this.nodeCfgJson.keyKey=contextKey;})" ><label>选择</label></el-link>&nbsp;
            </el-form-item>
		</el-col>
    </el-row>
    <el-row :gutter="4">
        <el-col :span="12">
            <el-form-item label="消息keyKey">
                <el-input v-model="nodeCfgJson.keyKey" size="small" />
            </el-form-item>
        </el-col>
        <el-col :span="12">
            <el-form-item label="消息key属性">
                <el-input v-model="nodeCfgJson.keyAttr" size="small" />
            </el-form-item>
        </el-col>
    </el-row>
    
    <el-row :gutter="4">
        <el-col :span="16">
            <el-form-item label="消息内容来源">
                <el-select v-model="nodeCfgJson.msgSource" clearable class="m-2" placeholder="Select" size="small">
                    <el-option v-for="item in sourceOptions" :key="item.value" :label="item.label" :value="item.value" />
                </el-select>
            </el-form-item>
        </el-col>
        <el-col :span="8">
		    <el-form-item label="" label-width="10px">
                <el-link  type="primary" @click="showAvailableDataSelectPage(sourceOptions, (contextType, contextKey)=>{this.nodeCfgJson.msgSource=contextType;this.nodeCfgJson.msgKey=contextKey;})" ><label>选择</label></el-link>&nbsp;
            </el-form-item>
		</el-col>
    </el-row>
    <el-row :gutter="4">
        <el-col :span="24">
            <el-form-item label="消息内容Key">
                <el-input v-model="nodeCfgJson.msgKey" size="small" />
            </el-form-item>
        </el-col>
